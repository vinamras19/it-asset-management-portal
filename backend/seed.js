const generateAssetTag = (category, index) => {
    const prefix = {
        Laptops: 'LAP',
        Desktops: 'DES',
        Monitors: 'MON',
        Servers: 'SRV',
        Mobile: 'MOB',
        Tablets: 'TAB',
        Networking: 'NET',
        Accessories: 'ACC',
    };
    const year = new Date().getFullYear();
    const num = String(index).padStart(4, '0');
    return `${prefix[category] || 'AST'}-${year}-${num}`;
};

const generateSerialNumber = (category, index) => {
    const chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789';
    let serial = category.substring(0, 2).toUpperCase();
    for (let i = 0; i < 10; i++) {
        serial += chars.charAt(Math.floor(Math.random() * chars.length));
    }
    return serial;
};

